public class HarryAge{
    public static void main(String[] args) {
        // Declaring variables
        String name = "Harry";
        int birthYear = 2000;
        int currentYear = 2024;

        // Calculating age
        int age = currentYear - birthYear;

        // Display result
        System.out.println(name + "'s age in " + currentYear + " is " + age);
}
}